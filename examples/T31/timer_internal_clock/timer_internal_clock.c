#include <hic.h>

#define CLRWDT()   {__Asm CWDT;}        //宏定义清狗指令

/******************************************************
函数名：void RAMclear(void)
描  述：RAM区数据清零，RAM区地址0x0000~0x03FF
输入值：无
输出值：无
返回值：无
*******************************************************/
void RAMclear(void)
{
    for (IAAH=0; IAAH<=0x03; IAAH++)
    {
        for (IAAL=0; IAAL<0xFF; IAAL++)
            IAD = 0x00;
        IAD = 0x00;
    }
}

/******************************************************
函数名：void GPIOInit(void)
描  述：首先初始化所有未用到的IO口为输出低电平
输入值：无
输出值：无
返回值：无
*******************************************************/
void GPIOInit(void)
{
    ANSL = 0xFF;        //选择对应端口为数字IO功能
	ANSH = 0xFF;
    PAT = 0x00;         //所有IO设为输出低电平
    PBT = 0x00;
    PCT = 0x00;
    PA = 0x00;
    PB = 0x00;
    PC = 0x00;
}

/*******************************************************
函数名：void isr(void) interrupt
描  述：中断服务程序
输入值：无
输出值：无
返回值：无
*******************************************************/
void isr(void) interrupt
{
    if (UPIS==1 && UPIF==1)        //定时器溢出中断
    {
        UPIC = 1;       //清溢出标志位，UPIF必须要在T31IF之前清除
        T31IF = 0;      //清定时器总标志位，UPIF必须要在T31IF之前清除

        PA1 = ~PA1;
    }
}

/*******************************************************
函数名：void main(void)
描  述：主函数，定时器1s溢出翻转PA1
输入值：无
输出值：无
返回值：无
*******************************************************/
void main(void)
{
    RAMclear();
    GPIOInit();
    T31C2L = 0x00;      //禁止从模式，使用内部HRC(16mHz)作为计数器时钟源
    T31C0L = 0x00;      //T31CNTLD寄存器无缓冲，写入值立即生效。计数器边沿计数模式，向上计数
    T31CNTLDH = 0xF4;     //重装载值，计数器与其匹配时产生事件
    T31CNTLDL = 0x24;
    T31PRSH = 0x00;       //预分频系数1:16
    T31PRSL = 0x0F;
    T31POS = 0x0F;        //计数器与T31CNTLD第16次匹配事件时产生溢出中断  
    UPIE = 1;       //打开计数溢出中断
    T31IE = 1;      //开启多功能定时器中断
    UPIC = 1;       //清溢出标志位
    T31IF = 0;      //清定时器总标志位
    GIE = 1;        //开启全局中断
    T31EN = 1;      //使能计数器
    while (1)
    {
        CLRWDT();
    }
}
